import { addBinary } from './67-add-binary';

describe('addBinary', () => {
  test('Input: a = "11", b = "1"; Output: "100"', () => {
    expect(addBinary('11', '1')).toBe('100');
  });

  test('Input: a = "1010", b = "1011"; Output: "10101"', () => {
    expect(addBinary('1010', '1011')).toBe('10101');
  });

  test('Input: a = "10100000100100110110010000010101111011011001101110111111111101000000101111001110001111100001101", b = "110101001011101110001111100110001010100001101011101010000011011011001011101111001100000011011110011"; Output: "10101"', () => {
    expect(
      addBinary(
        '10100000100100110110010000010101111011011001101110111111111101000000101111001110001111100001101',
        '110101001011101110001111100110001010100001101011101010000011011011001011101111001100000011011110011'
      )
    ).toBe('110111101100010011000101110110100000011101000101011001000011011000001100011110011010010011000000000');
  });
});
